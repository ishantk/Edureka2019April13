package co.edureka.threads;

// import java.lang.System; // Not Requried
// java.lang is fundamental package in java which contains all this built in code for us

public class LangPackageDemo {

	public static void main(String[] args) {
		
		System.out.println("Hello"); // System class resides in java.lang package which we need not to import
		
		System.out.println("pi: "+Math.PI);
		System.out.println("e: "+Math.E);

		System.out.println("Square Root of 25 is: "+Math.sqrt(25));
		// Mathematical APIs available in Math Class :)
		Math.cos(25);
		Math.tanh(25);
		
	}

}
